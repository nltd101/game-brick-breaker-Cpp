void GenerateMapBaseLevel::GenerateItem(int a[9][10])
{
	for (int i = 0; i < 15; i++)
	{
		int icoin = rand() % 9;
		int jcoin = rand() % 10;
		if (a[icoin][jcoin]!=0)
		{
			a[icoin][jcoin] = 4;
		}
	}
	for (int i = 0; i < 5; i++)
	{
		int icoin = rand() % 9;
		int jcoin = rand() % 10;
		if (a[icoin][jcoin] != 0)
		{
			a[icoin][jcoin] = 3;
		}
	}
	int countheart = 3;
	while (countheart!=0)
	{
		int icoin = rand() % 9;
		int jcoin = rand() % 10;
		if (a[icoin][jcoin] != 0)
		{
			a[icoin][jcoin] = 2;
			countheart--;
		}
	}
	for (int i = 0; i < 5; i++)
	{
		int icoin = rand() % 9;
		int jcoin = rand() % 10;
		if (a[icoin][jcoin] != 0)
		{
			a[icoin][jcoin] = 5;
		}
	}
};
